# include <stdio.h>

int main()
{
	int a = 47;

	printf("%x", a);   //输出结果为：2f
	printf("%X", a);   //输出结果为：2F
	printf("%#x", a);  //输出结果为：0x2f
	printf("%#X",a);   //输出结果为：0X2F   %#X推荐使用

	return 0;
}
/*
	在vc2019的输出结果为
——————————————————
2f
2F
0x2f
0X2F
	结论___________________
*/